    public void llenar() {
    Object[] ob = new Object[7];
    al = new ArrayList<Usuario>();
    String[] col = {"ID", "NOMBRE", "Apellido", "Edad", "Genero", "Corre", "Telefono"};
    DefaultTableModel tm = new DefaultTableModel(null, col);
    al.addAll(c.mostrar());
    for (Usuario u : al) {
        ob[0] = u.getId();
        ob[1] = u.getNombre();
        ob[2] = u.getApellido();
        ob[3] = u.getEdad();
        ob[4] = u.getGenero();
        ob[5] = u.getCorreo();
        ob[6] = u.getTelefono();

        tm.addRow(ob);
    }
    jTable3.setModel(tm); // Cambiar jTable1 por jTable3
}


